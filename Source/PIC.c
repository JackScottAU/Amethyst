#include <port.h>

void PIC_Remap(void)
{
    writeIO(0x20, 0x11);
    writeIO(0xA0, 0x11);
    writeIO(0x21, 0x20);
    writeIO(0xA1, 0x28);
    writeIO(0x21, 0x04);
    writeIO(0xA1, 0x02);
    writeIO(0x21, 0x01);
    writeIO(0xA1, 0x01);
    writeIO(0x21, 0x0);
    writeIO(0xA1, 0x0);
}
